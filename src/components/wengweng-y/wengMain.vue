<script type='text/ecmascript-6'>
    import "../../../static/css/weng-y.css"
    import "../../../static/libs/jquery-1.8.3.min.js"
    import "../../../node_modules/swiper/dist/js/swiper.min.js"
    import "../../../node_modules/swiper/dist/css/swiper.min.css"
    import Swiper from "swiper"
    import option from "./option.vue"
   
    export default {
    	components:{
    		opt:option
    	},
    	methods:{
    		//滚动固定ul
    		onScroll(){		
    			 let scrolled = document.documentElement.scrollTop || window.pageYOffset || document.body.scrollTop;
    			  // console.log(scrolled)
    			 var top2=$(".option").offset().top;
    			 if(scrolled>top2){
    			 	$(".nav").css("display","none");
    			 	$(".option").css({"position":"fixed","left":"0","top":"0.44rem","background":"white","z-index":"10"});
    			 }
    			 
    			 if(scrolled<80){
    			 	$(".nav").css("display","block")
    			 	$(".option").css("position","inherit");
    			 	
    			 }
    			 
    		} 
          
       },
    	mounted:function(){
    		
    		window.addEventListener('scroll', this.onScroll)

    		
    		//顶部拖动移动轮播图
    		var mySwiper = new Swiper('.swiper-container', {
				slidesPerView:3.5,
				spaceBetween:12
 			})
    		
    		//页面主要内容6个选项移动
    		var swiper2 = new Swiper('.content', {
    			//解决每个swiper高度不一致问题
    			autoHeight:true,
    		 
    			//onSlideChangeEnd每一个轮播图滑动结束后的回调函数
				onSlideChangeEnd:function(swiper){
					//先让所有的a标签removeClass
    				$(".option a").removeClass("active");
    				//取轮播图对应的下标添加样式:给回调函数传个参数swiper，swiper.activeIndex能取到当前下标
    				$(".option a").eq(swiper.activeIndex).addClass("active");

				}
	
    		})
    		
    		//点击切换页面大轮播图
    		$(".option a").click(function(){
    			//先让所有的a标签removeClass
    			  	$(".option a").removeClass("active");
    			//点击的a加上
    				$(this).addClass("active");
    			//找到当前点击的下标，显示不同的内容块
 					var idx=$(this).parent().index();
 				//!!!一定注意不能用点击显示隐藏的选项卡效果，要用slideTo
  					swiper2.slideTo(idx,300,false);
 			})
    		
    		//ajax请求数据
    		$.ajax({
    			url:"../../static/slection.json",
    			type:"get",
    			dataType:'json',
    			success:function(res){
    				//console.log(res);
    				for(var i=0;i<res.length;i++){
    				var str='<div class="one"><img src="'+res[i].img+'"/><p class="address iconfont icon-dingwei">'+res[i].title+'</p>'
       				+'<div class="dec"><p class="topic">#风儿随风飘#</p><p><img src="'+res[i].img+'" alt="" />多情的海湾</p></div></div>';
       				$('.slection').append(str);
       				$('.slection2').append(str);
       				$('.slection3').append(str);
       			}
    			}
    			
    		})

 
    }

    	
    	
    }
</script>

